// Yardımcı fonksiyonlar
export const getSiniflar = () => curriculumData.map(item => item.sinif);

export const getDersler = (sinif) => {
  const sinifData = curriculumData.find(item => item.sinif === sinif);
  return sinifData ? sinifData.dersler.map(ders => ders.ders) : [];
};

export const getKonular = (sinif, ders) => {
  const sinifData = curriculumData.find(item => item.sinif === sinif);
  if (!sinifData) return [];
  
  const dersData = sinifData.dersler.find(d => d.ders === ders);
  return dersData ? dersData.konular : [];
}; 